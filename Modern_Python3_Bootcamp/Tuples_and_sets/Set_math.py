
math_students = {"Matthew", "Helen", "James", "Oliver"}
biology_students = {"Jane", "Charlotte", "Oliver", "Matthew"}

# Union of two sets
# All students at once
print("\n********** All students in both classes **********\n")
print(math_students | biology_students)


# Intersection of two sets
# All students which are in both classes
print("\n********** Students which are in both classes **********\n")
print(math_students & biology_students)
