
alphabet = ("a", "b", "c", "d", "a")

print("\n********** Looping tuples **********\n")
for value in alphabet:
    print(value)
    
    
print("\n********** Counting tuples values **********\n")
print(alphabet.count("a"))


print("\n********** Returns index tuples values **********\n")
print(alphabet.count("a"))